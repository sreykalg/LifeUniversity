package String;

public class CharArrayMain {

    public static void main(String[] args) {
        char a = 'A';
        System.out.println(a);

        char[] charArr = {'H', 'e', 'l', 'l', 'o'};
        System.out.println(charArr);

        String str = "Hello";
        System.out.println(str);
    }
}