package variable;

public class Var5 {
    public static void main(String[] args) {
        // 변수 선언, 초기화 각각 따로
        int a; // 변수 선언
        a = 1; // 변수 초기화
        System.out.println(a);

        // 변수 선언과 초기화 같이
        int b = 2;
        System.out.println(b);

        // 여러 변수를 선언과 초기화 같이
        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);
    }
}